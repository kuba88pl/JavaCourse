package com.example;

public class Op4_logical_operator_and {
    public static void main(String[] args) {
        // koniunkcja - operator logiczny, inaczej AND

        System.out.println(5 < 10 && 20 >= 15);

        System.out.println(5 > 20 && 20 >= 15);

        System.out.println(5 < 20 && 20 <= 15);

        System.out.println(5 > 20 && 20 != 20);
    }
}
